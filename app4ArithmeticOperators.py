# Operações aritiméticas.
# Soma.
print(4 + 5);
# Subtração.
print(5 - 3);
# Multiplicação.
print(2 * 6);
# Divisão.
print(6 / 3);
# No caso de uma divisão com resto maior que zero, temos um número decimal.
print(8 / 3);
# Pode-se usar uma função para arredondar esse valor.
print(round(8 / 3));
# O resto da divisão.
print(5 % 3);
# Potenciação.
print(5 ** 2);