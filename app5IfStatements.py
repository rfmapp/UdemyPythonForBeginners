# Declarações condicionais com If.
# Variáveis para os exemplos;
a = 2;
b = 4;
# Declaração das condições de comparação.
if a == b:
    print("A é igual a B.")
if a != b:
    print("A é diferente de B.")
if a < b:
    print("A é menor que B.")
if a > b:
    print("A é maior que B.")
if a <= b:
    print("A é menor ou igual a B.")
if a >= b:
    print("A é maior ou igual a B.")
